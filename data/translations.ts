// Fallback mechanism: Fill missing translations with English
// This ensures all languages have all translation keys, even if not fully translated
const englishTranslations = translations[Language.English];
const englishKeys = Object.keys(englishTranslations);

Object.values(Language).forEach(lang => {
  if (lang !== Language.English) {
    const langTranslations = translations[lang];
    if (langTranslations) {
      englishKeys.forEach(key => {
        // Only fill if the key doesn't exist (not if it's empty string)
        if (!(key in langTranslations)) {
          langTranslations[key] = englishTranslations[key];
        }
      });
    }
  }
});
